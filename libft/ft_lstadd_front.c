#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (!new || !lst)
		return ;
	new->next = *lst;
	*lst = new;
}
// adds new structure to the beginning of a linked list
// double pointer is needed because we want to change 
// the pointer to the linked list outside of this function 
// (globally)
